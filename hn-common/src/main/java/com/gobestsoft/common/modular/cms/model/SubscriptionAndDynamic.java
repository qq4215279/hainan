package com.gobestsoft.common.modular.cms.model;

import java.util.List;

public class SubscriptionAndDynamic {
	private String id;

	private String name;

	private int is_new;

	private String avatar;

	private String describe;

	private String create_time;

	private int subscribed_total;

	private int subscribed;
	
	private String cover_url;
	
	private List<SubscriptionDynamic> dynamics;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getIs_new() {
		return is_new;
	}

	public void setIs_new(int is_new) {
		this.is_new = is_new;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getDescribe() {
		return describe;
	}

	public void setDescribe(String describe) {
		this.describe = describe;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

	public int getSubscribed_total() {
		return subscribed_total;
	}

	public void setSubscribed_total(int subscribed_total) {
		this.subscribed_total = subscribed_total;
	}

	public int getSubscribed() {
		return subscribed;
	}

	public void setSubscribed(int subscribed) {
		this.subscribed = subscribed;
	}

	public String getCover_url() {
		return cover_url;
	}

	public void setCover_url(String cover_url) {
		this.cover_url = cover_url;
	}

	public List<SubscriptionDynamic> getDynamics() {
		return dynamics;
	}

	public void setDynamics(List<SubscriptionDynamic> dynamics) {
		this.dynamics = dynamics;
	}

	
}
